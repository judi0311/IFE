				<div class="boxsmallcontent">
				  <div class="clear">
				    <P>吉林大学绿色能源实验室始建于2000年，其前身是“清洁能源与相关技术”中俄联合实验室。实验室现有专属研究人员20余人，其中外专千人1人，吉林省长白山学者1人，唐敖庆特聘全职教授4人，教授9人，外籍全职教授5人，副教授1人，高级工程师3人。另外团队每年可招收硕士研究生20余人、博士研究生7人，外籍研究生3~5人。实验室主要致力于绿色能源（太阳能、风能）的高效利用、储存及应用性基础研究，研究目标是如何高效地将绿色能源如太阳能和风能转化为氢能，为破解光伏发电和风能发电上网难和弃电率高等难题提供一个全新的解决途径。团队通过将光伏发电和风力发电的剩余电力，通过电解水制造氢气，再将制出的氢储存在供气网中，在电网需要发电的时候再通过燃料电池技术转换成为电能。课题组围绕电产氢和氢产电进行全链条的研究，主要细分为以下几个研究方向：</P>
<P>1）清洁能源（太阳能、风能）制氢全链条系统研发；</P>
<P>2）高效硅基太阳能电池的制备、改性与应用研发；</P>
<P>3）超级电容器器件及其模组器件研发和其在新能源存储和电动汽车领域的应用研发；</P>
<P>4）电解水制氢电极及其高压氢制备系统研发；</P>
<P>5）新型固体燃料电池的设计与研发；</P>
<P>6）电产氢和氢产电全链条相关电气及汽车电子与仪器仪表研发；</P>
<P>7）新型锂/钠离子电池电极设计与模组研发。</P>
<P>团队围绕上述研究内容培养创新性应用型人才，毕业人员主要去往高新技术企业，高校和科研院所从事科学研究、技术开发等相关工作。
绿色能源科研团队是一个年轻，富有创新精神的国际化研究团队。实验室充分利用俄罗斯及其它独联体的科技和人才优势，联合吉林大学化学学院、材料学院、汽车学院、地球科学院、地探学院、建工学院、环境与资源学院等单位，已经成功建成一个跨院系、多学科交叉的联合机构，这样既能充分发挥吉林大学学科门类齐全的优势，又能全面推动吉林大学与俄罗斯（独联体）国家科研领域的全面合作。
欢迎有志青年报考硕士和博士研究生，欢迎相关领域的博士和教师到本课题组从事博士后合作研究和访问学者合作交流，让我们一起探索充满挑战的未来绿色新能源。</P>

